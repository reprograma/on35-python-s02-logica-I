#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
sem01 = float(input ("Insira primeira nota: "))
sem02 = float(input ("Insira segunda nota: "))
sem03 = float(input ("Insira terceira nota: "))
sem04 = float(input ("Insira quarta nota: "))

media = (sem01 + sem02 + sem03 + sem04) / 4

print(f"A média das notas bimetrais é {media}")