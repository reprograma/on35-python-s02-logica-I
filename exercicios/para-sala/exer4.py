#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota1 = float('Insira sua primeira nota ')
nota2 = float('Insira sua segunda nota ')
nota3 = float('Insira sua terceira nota ')
nota4 = float('Insira sua quarta nota ')

media = (int(nota1) + int(nota2) + int(nota3) + int(nota4)) / 4

print(f'Sua média é {media}')